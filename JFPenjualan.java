    private void txtKodePelangganKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKodePelangganKeyReleased
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();
            if(!(((karakter >= '0') && (karakter <= '9') || (karakter == KeyEvent.VK_BACK_SPACE) || (karakter == KeyEvent.VK_DELETE)))){
                getToolkit().beep();
                evt.consume();
            }
                String obt = txtKodePelanggan.getText();
                int obt1 = Integer.parseInt(obt);
        try {
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/apotek_1", "root", "");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from pelanggan where kode = '"+txtKodePelanggan.getText()+"'");
            if (rs.next()) {
                txtNamaPelanggan.setText(rs.getString("nama"));
                if (obt1>0) {
                jenisobat.setText("Antibiotik");
            }
                else if(obt1==0){
                jenisobat.setText("Random");
            }
            }else{
                jenisobat.setText("Random");
                txtNamaPelanggan.setText("");
            }
            con.close();
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_txtKodePelangganKeyReleased

