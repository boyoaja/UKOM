    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/apotek_1", "root", "");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select jumlah from barang where kode = '"+txtKodeBarang.getText()+"'");
            if (rs.next()) {
                jml1.setText(rs.getString("jumlah"));
                String jml = jml1.getText();
                int jml2 = Integer.parseInt(jml);
                int jml3 = Integer.parseInt(txtJumlahBarang.getText());
                int jml4 = jml2-jml3;
                if (jml4<0) {
                    JOptionPane.showMessageDialog(null,"Stok Barang Habis atau Tidak mencukupi","Informasi",JOptionPane.INFORMATION_MESSAGE);
                    
                } else if(jml4>=0){
                    penjualan.addtmp(this);
                    penjualan.isiTabeltmp();
                    txtJumlahBarang.setText("");
                    txtTotalHarga.setText("");
                }
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(p_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()>=0){
            int dialogResult = JOptionPane.showConfirmDialog (this, "Anda Yakin Akan Menghapus Data Ini?","Peringatan",JOptionPane.WARNING_MESSAGE);
            if(dialogResult == JOptionPane.YES_OPTION){
                String id = jTable1.getValueAt(jTable1.getSelectedRow(),0).toString();
                penjualan.deletet(id);
                penjualan.isiTabelt();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Data Pada Tabel Sebelum Hapus","Pesan Kesalahan",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

