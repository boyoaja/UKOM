    private void ubahMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ubahMousePressed
        // TODO add your handling code here:
        try {
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/apotek_1", "root", "");
            java.sql.Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from login where username = '"+ubah.getText()+"'");
            if (rs.next()) {
                txtNamaDepan.setText(rs.getString("firstname"));
                txtNamaBelakang.setText(rs.getString("lastname"));
                txtUsername.setText(rs.getString("username"));
                txtPassword.setText(rs.getString("password"));
                comboOption.setSelectedItem(rs.getString("options"));
            } else {
            }
            
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(p_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ubahMousePressed

