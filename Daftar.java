    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        // TODO add your handling code here:
        String fn = txtNamaDepan.getText();
        String ln = txtNamaBelakang.getText();
        String un = txtUsername.getText();
        String psw = txtPassword.getText();
        String ot = (String) comboOption.getSelectedItem();
        
        if (fn.equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Nama Depan Tidak Boleh Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        if (ln.equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Nama Belakang Tidak Boleh Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        if (un.equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Username Tidak Boleh Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        if (psw.equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Password Tidak Boleh Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        if (ot.equals("")) {
            JOptionPane.showMessageDialog(null, "Option Tidak Boleh Kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }

       try{
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/apotek_1", "root", "");
            com.mysql.jdbc.Statement st = (com.mysql.jdbc.Statement) con.createStatement();
            ResultSet rs = st.executeQuery("Select * from login where username = '"+un+"'");
            rs.next();
            if (rs.getRow()>0){
                st.close();
                rs.close();
                JOptionPane.showMessageDialog(null,"Username Sudah Ada","Informasi",JOptionPane.INFORMATION_MESSAGE);
            } else  {
                st.close();
                rs.close();
                PreparedStatement ps = (PreparedStatement) con.prepareStatement("insert into login (firstname,lastname,username,password,options) values(?,?,?,?,?)");
                ps.setString(1, fn);
                ps.setString(2, ln);
                ps.setString(3, un);
                ps.setString(4, psw);
                 ps.setString(5, ot);
                ps.executeUpdate();
                con.close();
                dt_anggota();
            }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null,"Data sudah tersimpan","Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
       txtNamaDepan.setText("");
        txtNamaBelakang.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        comboOption.setSelectedItem("-Select-");
       
    }//GEN-LAST:event_btn_AddActionPerformed

